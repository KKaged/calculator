function sum(a, b) {
  let result = a + b;
  return result;
}
console.log(sum(1, 1));

function sub(a, b) {
  let result = a - b;
  return result;
}
console.log(sub(6, 2));

function multiply(a, b) {
  let result = a * b;
  return result;
}
console.log(multiply(2, 3));

function divide(a, b) {
  let result = a / b;
  return result;
}
console.log(divide(16, 2));
